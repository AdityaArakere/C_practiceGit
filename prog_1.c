#include<stdio.h>
int main()
{
    printf("enter the character\n");
    char ch;
    scanf("%c",&ch);

    switch (ch)
    {
        case 'a':
        
        case 'e':
        
        case 'i':
            
        case 'o':
            
        case 'u':
            printf("entered vowel");
            break;
            
        default:
            printf("not a vowel");
            break;
    }

    return 0;
}
